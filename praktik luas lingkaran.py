#input user
jari_jari = float(input("masukan jari_jari: ")) #user memasukkan panjang jari_jari lingkaran

phi = float("3.14") #menetapkan bilangan phi

#variabel rumus
luas = jari_jari * jari_jari * phi #rumus luas lignkaran
keliling = 2 * phi * jari_jari  #rumus keliling lingkaran

#hasil luas lingkaran
print("luas lingkaran: ", luas)
#hasil keliling lingkaran
print ("keliling lingkaran: ", keliling)
